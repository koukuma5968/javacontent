package example;

public class Chapter3_02 {

    public static void main(String[] args) {

        int numA = 2;
        int numB = 3;

        // 二つの値の和を求める
        System.out.println(numA + numB);

        // 二つの値の差を求める
        System.out.println(numA - numB);

        // 二つの値の積を求める
        System.out.println(numA * numB);

        // 二つの値の商を求める
        System.out.println(numA / numB);

        // 二つの値の余りを求める
        System.out.println(numA % numB);

        // 正負の符号を反転する
        System.out.println(-numA);

    }

}
