package example;

public class Chapter3_01 {

    public static void main(String[] args) {

        int num;

        // 右辺の値を代入
        num = 1;
        System.out.println(num);

        // 右辺の値を加算
        num += 4;
        System.out.println(num);

        // 右辺の値を減算
        num -= 3;
        System.out.println(num);

        // 右辺の値を乗算
        num *= 5;
        System.out.println(num);

        // 右辺の値を除算
        num /= 2;
        System.out.println(num);

        // 右辺の値で割った余りを代入する
        num %= 3;
        System.out.println(num);

    }

}
